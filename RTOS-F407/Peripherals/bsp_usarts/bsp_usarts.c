#include "bsp_usarts.h"


static void Debug_USART_Config(void){
	
  GPIO_InitTypeDef   GPIO_InitStructure;
  USART_InitTypeDef  USART_InitStructure;
		
  RCC_AHB1PeriphClockCmd( DEBUG_USART_RX_GPIO_CLK |DEBUG_USART_TX_GPIO_CLK, ENABLE);
 
  RCC_APB2PeriphClockCmd(DEBUG_USART_CLK, ENABLE);																			/* 使能 UART 时钟 */
 
  GPIO_PinAFConfig(DEBUG_USART_RX_GPIO_PORT,DEBUG_USART_RX_SOURCE, DEBUG_USART_RX_AF);	/* 连接 PXx 到 USARTx_Tx*/
 
  GPIO_PinAFConfig(DEBUG_USART_TX_GPIO_PORT,DEBUG_USART_TX_SOURCE, DEBUG_USART_TX_AF);	/* 连接 PXx 到 USARTx__Rx*/
 
  GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;  																			/* 配置Tx引脚为复用功能  */
  GPIO_InitStructure.GPIO_PuPd  = GPIO_PuPd_UP;
  GPIO_InitStructure.GPIO_Mode  = GPIO_Mode_AF;

  GPIO_InitStructure.GPIO_Pin   = DEBUG_USART_TX_PIN;
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
  GPIO_Init(DEBUG_USART_TX_GPIO_PORT, &GPIO_InitStructure);

  GPIO_InitStructure.GPIO_Mode  = GPIO_Mode_AF;   																			/* 配置Rx引脚为复用功能 */
  GPIO_InitStructure.GPIO_Pin   = DEBUG_USART_RX_PIN;
  GPIO_Init(DEBUG_USART_RX_GPIO_PORT, &GPIO_InitStructure);
  
  USART_InitStructure.USART_BaudRate   = DEBUG_USART_BAUDRATE; 													/* 配置串DEBUG_USART 模式 */
  USART_InitStructure.USART_WordLength = USART_WordLength_8b;
  USART_InitStructure.USART_StopBits   = USART_StopBits_1;
  USART_InitStructure.USART_Parity     = USART_Parity_No ;
  USART_InitStructure.USART_HardwareFlowControl = USART_HardwareFlowControl_None;
  USART_InitStructure.USART_Mode       = USART_Mode_Rx | USART_Mode_Tx;
  USART_Init(DEBUG_USART, &USART_InitStructure); 
  
	USART_Cmd(DEBUG_USART, ENABLE);
	
}


static void NVIC_Configuration(void){
	
  NVIC_InitTypeDef NVIC_InitStructure;
  
  NVIC_PriorityGroupConfig(NVIC_PriorityGroup_2);											/* 嵌套向量中断控制器组选择 */
  
  NVIC_InitStructure.NVIC_IRQChannel 									 = USART1_IRQn;	/* 配置USART为中断源 */
  NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority = 1;					 	/* 抢断优先级为1 */
  NVIC_InitStructure.NVIC_IRQChannelSubPriority 			 = 1;						/* 子优先级为1 */
  NVIC_InitStructure.NVIC_IRQChannelCmd								 = ENABLE;			/* 使能中断 */
  
  NVIC_Init(&NVIC_InitStructure);																			/* 初始化配置NVIC */

}


void usart1_config(void){
	
	Debug_USART_Config();
	NVIC_Configuration();
	
}


void USART_Send_Bytes(USART_TypeDef* USARTx, u8* Data,u8 size){
  u8 i=0;
  for(i=0;i<size;i++){
    USART_SendData(USARTx,*Data++);
    while(USART_GetFlagStatus(USARTx,USART_FLAG_TXE) == RESET);
  }
}


void DEBUG_USART_Send_Bytes(uint8_t* pData, uint16_t DataLen){
 
	vTaskSuspendAll();
	USART_Send_Bytes(DEBUG_USART,pData,DataLen);
	xTaskResumeAll ();
	
}

